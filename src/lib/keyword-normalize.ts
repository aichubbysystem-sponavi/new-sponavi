/**
 * キーワードの表記ゆれ吸収（全角スペース・連続空白 → 半角スペース1つ、前後trim）。
 *
 * シート・グリッド計測・手動生成でスペースの種類が混在し、同一キーワードが
 * 別物として扱われる問題（例: 「名東区 モーニング」と「名東区　モーニング」が
 * チップに2つ並ぶ / 片方だけ「データなし」になる）を防ぐ。
 * ※ JavaScriptの \s は全角スペース(U+3000)にもマッチする
 */
export function normalizeKw(s: string): string {
  return s.replace(/\s+/g, " ").trim();
}
